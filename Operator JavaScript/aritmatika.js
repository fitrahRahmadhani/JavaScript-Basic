// Operator Aritmatika
console.log("Operator Aritmatika");

// Penjumlahan
let bilangan1 = 10;
let bilangan2 = 3;
console.log(bilangan1 + bilangan2);

//Pengurangan
console.log(bilangan1 - bilangan2);

// Perkalian
console.log(bilangan1 * bilangan2);

// Pembagian
console.log(bilangan1 / bilangan2);

// Eksponen (pangkat)
console.log(bilangan1 ** bilangan2);

// Modulus
console.log(bilangan1 % bilangan2);

// Increment
bilangan1++;
console.log(bilangan1);

// Decrement
bilangan1--;
console.log(bilangan1);