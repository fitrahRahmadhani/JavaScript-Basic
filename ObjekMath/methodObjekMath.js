// Objek Math yang digunakan untuk merubah nilai negatif menjadi positif

// Math.abs(x)
let bilangan = -70.5;
console.log(Math.abs(bilangan));

// Digunakan untuk menghitung hasil x pangkat y
// Math.pow(x,y)
let bilangan1 = 6;
let pangkat = 2;
console.log(Math.pow(bilangan1, pangkat));

// Digunakan untuk menghitung akar kuadrat dari x
// Math.sqrt(x)
let bilangan2 = 64;
console.log(Math.sqrt(bilangan2));

// Digunakan untuk menghitung akar pangkat 3 dari x
// Math.cbrt
let bilangan3 = 8;
console.log(Math.cbrt(bilangan3));

// Digunakan untuk membulatkan angka desimal x menjadi bilangan bulat (if >= 5 ke atas)
// Math.round
console.log(Math.round(Math.abs(bilangan)));

// Digunakan untuk membulatkan angka desimal x menjadi bilangan bulat (kebawah)
// Math.floor
console.log(Math.floor(Math.abs(bilangan)));

// Digunakan untuk membulatkan angka desimal x menjadi bilangan bulat (keatas)
// Math.ceil
console.log(Math.ceil(Math.abs(bilangan)));

// Digunakan untuk menampilkan angka secar acak antara 0 hingga 1 (0 dan 1 tidak termasuk)
// Math.random
console.log(Math.random());

// Digunakan untuk mencari angka terbesar dari parameter yang dimasukkan
// Math.max
console.log(Math.max(1,4,2,7,5,6));

// Digunakan untuk mencari angka terkecil dari parameter yang dimasukkan
// Math.min
console.log(Math.min(1,4,2,7,5,6))